    private void tSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tSearchActionPerformed
        Admin a = new Admin();  //create object

        // Check if the student ID exists using the checkStudentID() method
        if(a.checkStudentID(tID.getText())==true){  // If the student ID exists
            String studentInfo = a.accessStudentInfo(tID.getText());
            printStudentInfo.setText(studentInfo);  // Display the student information in the printStudentInfo component
        }
        else{
            // If the student ID does not exist
            JOptionPane.showMessageDialog(this, tID.getText()+" not found.");
        }
    }//GEN-LAST:event_tSearchActionPerformed

