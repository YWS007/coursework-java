    private void bViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bViewActionPerformed
        // Clear existing rows from the jTable
        model.setRowCount(0);
        
        List<Object> studentRoom = new ArrayList<>();
        WriteAndReadFile w = new WriteAndReadFile("Approve.txt");
        w.read(studentRoom);    //read data
        
        List<Object> roomInfo = new ArrayList<>();
        WriteAndReadFile w2 = new WriteAndReadFile("Room.txt");
        w2.read(roomInfo);  //read data
        
        //display all the data in Jtable
        for(int i=0; i<studentRoom.size(); i+=5){
            for(int j=0; j<roomInfo.size(); j+=5){
                if(roomInfo.get(j).equals(studentRoom.get(i+2))){
                    Object[] record = {studentRoom.get(i),studentRoom.get(i+2), roomInfo.get(j+4),studentRoom.get(i+3), studentRoom.get(i+4)};
                    model.addRow(record);   //in the string array
                }
            }
        }
     
    }//GEN-LAST:event_bViewActionPerformed

